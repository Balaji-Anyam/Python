#Compare two integers and print boolean results for each comparison (==, !=, >, <, >=, <=).

a = int(input("Enter a number: "))
b = int(input("Enter another number: "))

print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)